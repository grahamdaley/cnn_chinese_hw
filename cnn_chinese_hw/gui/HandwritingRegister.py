LOrds = []
PATH = f'{get_package_dir()}/data/supplemental_data.json'


class HandwritingRegister:
    def __init__(self):
        self.DStrokes = self.get_D_strokes()
        self.LOrds = [i for i in LOrds if not i in self.DStrokes]

    def get_next_char(self):
        i = self.LOrds[0]
        del self.LOrds[0]
        return i

    def get_D_strokes(self):
        DStrokes = {}

        if os.path.exists(PATH):
            with open(PATH, 'r', encoding='utf-8') as f:
                for line in f:
                    if not line.strip():
                        continue
                    ord_, LLines = json.loads(line)
                    DStrokes.setdefault(ord_, []).append(LLines)
        return DStrokes

    def add_char(self, ord_, LLines):
        with open(PATH, 'a+', encoding='utf-8') as f:
            f.write(json.dumps([ord_, LLines])+'\n')
        self.DStrokes.setdefault(ord_, []).append(LLines)


